A política de privacidade tem como objetivo deixar claro o compromisso do website <b>MotoPod</b> em garantir a proteção de dados eventualmente solicitados.
<br>
-Para navegar em áreas restritas do <b>MotoPod</b> e ter acesso a alguns serviços como cadastros de motos e notícias, é necessário se registrar.<br>
<br>
Não fornecemos suas informações pessoais para terceiros e não divulgamos informações que possam identificar os usuários. Somente dados genéricos e agregados, referentes à quantidade de pessoas que acessam áreas do nossos site, podem ser divulgados para anunciantes e parceiros comerciais.<br>
<br>
Utilizamos seu e-mail apenas para enviar newsletters ou informar sobre nossas promoções e/ou de nossos parceiros, desde que você nos dê autorização para isso.<br>
<br>
Utilizamos essas informações para melhorar constantemente o conteúdo do nosso site e veicular propagandas que sejam de interesse dos nossos usuários.<br>
<br>
Para facilitar o seu acesso será gerado um cookie no seu computador. O cookie é um arquivo que será lido toda vez que você acessar o <b>MotoPod</b>. 
Dessa forma será dada permissão automática para a utilização de todas as áreas do <b>MotoPod</b>.<br>
<br>
Essas regras sobre privacidade se aplicam apenas ao <b>MotoPod</b>. Elas não valem para os sites que indicamos por meio de links. Você deve se informar sobre a política de cada um deles antes de fornecer qualquer tipo de informação.<br>
